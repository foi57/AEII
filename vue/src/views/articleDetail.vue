<script setup>
import { ref, onMounted } from 'vue'
import { useRoute } from 'vue-router'
import article from '../../api/article.js'
import { ElCard, ElLink } from 'element-plus' // 新增导入组件
import serverUrl from "../../serverUrl.js";
import Header from "../components/header.vue";
import Comment from "../components/releaseComment.vue";
import comments from "../../api/comments.js";
import CommentList from "../components/commentList.vue"; // 新增服务器地址配置

const route = useRoute()
const articleDetail = ref({})
// 添加一个ref用于触发评论列表刷新
const refreshComments = ref(0)

// 处理评论发布成功事件
const handleCommentSuccess = () => {
  // 增加计数器值，触发评论列表组件的更新
  refreshComments.value++
}

onMounted(async () => {
  try {
    const res = await article.selectArticleDetail(route.params.id)
    articleDetail.value = res.data
  } catch (err) {
    console.error('获取文章详情失败:', err)
  }
})


</script>

<template>
  <Header/>
  <div class="article-container">
    <h1>{{ articleDetail.articleTitle }}</h1>
    <div class="meta-info">
      <span>作者：{{ articleDetail.userName }}</span>
      <span>发布日期：{{ articleDetail.articleReleased }}</span>
    </div>
    <div class="content" v-html="articleDetail.articleContent"></div>

    <!-- 新增附件展示区块 -->
    <el-card v-if="articleDetail.attachments?.length" class="attachments-card">
      <template #header>
        <div class="card-header">
          <span>相关附件</span>
        </div>
      </template>
      <div v-for="(file, index) in articleDetail.attachments" :key="index" class="attachment-item">
        <el-link
          :href="serverUrl.url + file.url"
          target="_blank"
          type="primary"
          :title="file.name"
        >
          📎 {{ file.name }}
        </el-link>
      </div>
    </el-card>
  </div>
  <div>
    <h3>交流区</h3>
    <div class="comment-section">
      <!-- 添加@comment-success事件监听 -->
      <Comment @comment-success="handleCommentSuccess"/>
      <!-- 将refreshComments传递给评论列表组件 -->
      <comment-list :refresh-trigger="refreshComments" />
    </div>
  </div>
</template>

